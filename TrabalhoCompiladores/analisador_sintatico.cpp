void syntaxError(){
    cout << "Ocorreu um erro de sintaxe na linha: " << currentLine << endl;
    exit(1);
}

void parse(){
    initializeActionTable();
    int final_state = 1;
    int q = 0;
    stateStack.push(q);
    int a = nextToken();
    do{
        //cout << "PARSER: " << "STATE " << q << "token " << a << " name " << t_terminalNames[a] << endl;
        if(actionTable[q].count(a) < 1){
            syntaxError();
        }
        else{
            int p = actionTable[q][a];
            if(IS_SHIFT(p)){
                stateStack.push(p);
                a = nextToken();
            }
            else if(IS_REDUCTION(p)){
                int r = RULE(p);
                //cout << "RULE " << r << endl;
                for(int i = 0;i<ruleLen[r];i++){
                    //cout << "POP " << stateStack.top() << endl;
                    stateStack.pop();
                }
                stateStack.push(actionTable[stateStack.top()][ruleLeft[r]]);
                semantics(r);
            }
            else{
                syntaxError();
            }
            q = stateStack.top();
        }
    }while(q != final_state);
}



